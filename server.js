// TTL Management Functions
async function setEmailTTL(email, isManual = false) {
  if (!redisConnected) return;
  
  try {
    const ttlSeconds = isManual ? config.ttl.manualCreated : config.ttl.autoGenerated;
    await redisClient.expire(`inbox:${email}`, ttlSeconds);
    logger.info(`Set TTL for ${email}: ${ttlSeconds} seconds (${isManual ? 'manual' : 'auto-generated'})`);
  } catch (error) {
    logger.error(`Failed to set TTL for ${email}: ${error.message}`);
  }
}

async function updateEmailTTL(email, ttlSeconds) {
  if (!redisConnected) return false;
  
  try {
    await redisClient.expire(`inbox:${email}`, ttlSeconds);
    logger.info(`Updated TTL for ${email}: ${ttlSeconds} seconds`);
    return true;
  } catch (error) {
    logger.error(`Failed to update TTL for ${email}: ${error.message}`);
    return false;
  }
}

