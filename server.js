// Email receiving webhook endpoint
app.post('/webhook/receive', validateApiKey, async (req, res) => {
  try {
    const { to, from, subject, text, html, timestamp } = req.body;
    
    if (!to) {
      return res.status(400).json({
        error: 'Missing recipient email',
        message: 'The "to" field is required'
      });
    }
    
    // Create email message object
    const emailMessage = {
      from: from || 'unknown@sender.com',
      subject: subject || 'No Subject',
      text: text || '',
      html: html || '',
      timestamp: timestamp || new Date().toISOString(),
      id: crypto.randomBytes(16).toString('hex')
    };
    
    if (redisConnected) {
      // Store the email in Redis
      await redisClient.rPush(`inbox:${to}`, JSON.stringify(emailMessage));
      
      // Check if TTL exists, if not set default TTL
      const currentTTL = await redisClient.ttl(`inbox:${to}`);
      if (currentTTL === -1) {
        // No TTL set, apply default auto-generated TTL
        await setEmailTTL(to, false);
      }
      
      // Broadcast real-time notification
      broadcastNewEmail(to, emailMessage);
      
      logger.info(`Email received and stored for ${to} from ${emailMessage.from}`);
      
      res.json({
        message: 'Email received and stored successfully',
        recipient: to,
        messageId: emailMessage.id,
        timestamp: emailMessage.timestamp,
        realTimeNotified: true
      });
    } else {
      logger.warn(`Email received for ${to} but Redis not connected - email lost`);
      res.status(503).json({
        error: 'Redis not connected',
        message: 'Cannot store email - Redis service unavailable'
      });
    }
  } catch (error) {
    logger.error(`Failed to receive email: ${error.message}`);
    res.status(500).json({
      error: 'Failed to process incoming email',
      message: error.message
    });
  }
});

